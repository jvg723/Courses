#include <iostream>
#include <iomanip>

using namespace std;

int main (int argc, char * const argv[]) 
{
    // Here are two integer variables
    int value1 = 11;
    int value2 = 22;
	
    cout << "Bitwise OR of 11 and 22 (11 | 22) is " << (value1 | value2) << endl;
    return 0;
}
